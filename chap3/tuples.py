#  A built-in data types that let us immutable sequence of values
# you can also do silicing in tuples  like list

numbers = (6, 5, 4, 3, 6, 6, 3)

print(numbers[0:4])


# methods which are used for tuples
print(numbers.index(4)) # this mthod will find the index of an element in tuple where it first occur
print(numbers.count(6))  #this method will return the occurence of an element that how many times it occurs in tuple