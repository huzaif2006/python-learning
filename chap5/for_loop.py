# for loop is used for iterate the list tupled and string, the syntax of for loop is given below

# for loop with list 

L = ["cake", "chocolate", "candy", "wafers"]

for i in L:
    print(i)





# for loop with tuple

T = ("BBQ", "Burger", "Pizza", "Broast")

for i in T:
    print(i)







# for loop with string 

S = "Huzaif"
for i in S:
    print(i)






